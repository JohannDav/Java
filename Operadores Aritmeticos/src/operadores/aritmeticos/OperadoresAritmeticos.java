package operadores.aritmeticos;
public class OperadoresAritmeticos {
    public static void main(String[] args) {
        
        int num1, num2, resultadosum, resultadores, resultadomul, resultadodiv;
        num1 = 4;
        num2 = 2;
        
        resultadosum = num1 + num2;
        System.out.println("El resultado de la suma es de:" +resultadosum);
        
        resultadores = num1 - num2;
        System.out.println("El resultado de la resta es de:" +resultadores);
        
        resultadomul = num1 * num2;
        System.out.println("El resultado de la multiplicacion es de: " +resultadomul);
        
        resultadodiv = num1 / num2;
        System.out.println("El resultado de la division es de: " +resultadodiv);
    }
    
}
